/**
 * op_add - computes addition of two integers
 * @a: first integer
 * @b: second integer
 * Return: value
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - computes subtraction of two integers
 * @a: first integer
 * @b: second integer
 * Return: value
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - computes multilplication of two integers
 * @a: first integer
 * @b: second integer
 * Return: value
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - computes division of two integers
 * @a: first integer
 * @b: second integer
 * Return: value
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - computes modulo of two integers
 * @a: first integer
 * @b: second integer
 * Return: value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
